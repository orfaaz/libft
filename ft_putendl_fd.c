#include "libft.h"

void	ft_putendl_fd(char *str, int fd)
{
	int	i;

	if (!str || fd < 0)
		return ;
	i = 0;
	while (*str)
	{
		write(fd, &(*str), 1);
		str++;
	}
	write(fd, "\n", 1);
}
